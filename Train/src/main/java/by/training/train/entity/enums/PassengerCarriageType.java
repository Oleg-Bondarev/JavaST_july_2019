package by.training.train.entity.enums;

/**Enum with type of passenger carriages.
 * SV - soft with double compartments.
 * K - four-seater coupe.
 * PL - non-compartment with berths (reserved seat).
 * O - non-compartment with seating (common).*/
public enum PassengerCarriageType { SV, K, PL, O }
